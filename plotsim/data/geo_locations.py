"""plotsim.data.geo_locations — curated reference dataset for the geo provider.

Each entry pairs a city with the country, country_code (ISO 3166-1 alpha-2),
region (state / province / equivalent), postcode, and latitude/longitude.
The full record is what the geo provider draws as a row-level "bundle" so a
dim row that requests both ``geo.country`` and ``geo.city`` always emits a
city that is in the stated country.

Why a curated list: M9d's spec rules out geocoding API calls and exhaustive
global coverage. Roughly 250 entries across 15 countries is enough breadth
for portfolio / demo dim tables ("here are 200 store locations") while
staying small enough to read at a glance and audit by hand. The
postcode/region pairings were chosen to be plausible cell references for
each city — they are not guaranteed to be the postcode the local post
office actually uses; they are realistic-looking *examples* of the format.

Determinism: this module is constants only. The :func:`numpy.random.Generator`
threaded into ``build_all_dimensions`` is the only source of randomness;
this dataset is the population it samples from.
"""

